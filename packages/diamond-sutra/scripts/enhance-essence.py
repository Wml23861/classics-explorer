def process_chapter(chapter_num):
    filepath = os.path.join(CHAPTERS_DIR, f'{chapter_num:02d}.ts')
    if not os.path.exists(filepath):
        print(f"File not found: {filepath}")
        return

    with open(filepath, 'r', encoding='utf-8') as f:
        content = f.read()

    # Find the essence field and replace it
    # The essence field starts with "  essence:" and ends before the next top-level field at the same indentation
    # We need to find where essence starts and where scenarios/questions/etc starts after it

    # Pattern: find "  essence:\n" and the multiline string that follows
    # The essence is a multiline single-quoted string with \n\n' + continuations

    # Use regex to find the start
    pattern = r'(  essence:\n)(    \'.*?)(?=\n  scenarios:|\n  questions:|\n  tags:|\n  classicsBridge:|\n  ruShiDao:|\n  insights:|\n  modernPractice:|\n  higherDimension:|\n  wangYangming:|\n  huineng:|\n  modernMasters:|\n  questionAnswers:|\n  relationshipWisdom:|\n  creatorNote:|\n  fiveMirrors:|\n  fourFoldAnalysis:|\n  celebrityAnalysis:|\n  meditationGuide:|\n  energyPractice:|\n  awakeningInsight:)'

    new_essence = ENHANCED.get(chapter_num)
    if not new_essence:
        print(f"No enhanced content for chapter {chapter_num}")
        return

    replacement = f'  essence:\n{new_essence}'

    # Use regex replacement
    new_content, count = re.subn(pattern, replacement, content, count=1, flags=re.DOTALL)

    if count == 0:
        print(f"WARNING: Could not find essence pattern in chapter {chapter_num}")
        return

    with open(filepath, 'w', encoding='utf-8') as f:
        f.write(new_content)

    print(f"Enhanced chapter {chapter_num}: {filepath}")


